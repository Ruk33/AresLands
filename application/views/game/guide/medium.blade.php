<h4>Misiones</h4>
<p>Algunas criaturas necesitan realizar algunos trabajos, ya sean de asesinato por la noche, llevar objetos, hablar con otras criaturas, enviar un mensaje y demás, por lo que contratan a personajes para realizarlos y pagarles una considerable recompensa al finalizarlos.</p>

<h4>Atributos</h4>
<p>Los atributos son los aspectos en los que puedes mejorar a tu personaje. Dependiendo de como los distribuyas los resultados serán diferentes.</p>

<ul style="margin-bottom: 25px;">
	<li style="margin-bottom: 10px;">
		<b style="color: #FF7A00;">Fuerza física:</b>
		Aumenta el poder de ataques físicos. Si posees mas fuerza que magia, tu personaje golpeará únicamente con ataques físicos.
	</li>
	
	<li style="margin-bottom: 10px;">
		<b style="color: #FDC535;">Destreza física:</b>
		Aumenta tu velocidad de golpeo en las batallas, pudiendo lograr así múltiples ataques consecutivos si eres mas rápido que tu adversario.
	</li>
	
	<li style="margin-bottom: 10px;">
		<b style="color: #9EDB1C;">Resistencia física:</b>
		Aumenta tu defensa contra ataques físicos. Cuanto mas resistencias tengas, menos daño te harán los ataques físicos.
	</li>
	
	<li style="margin-bottom: 10px;">
		<b style="color: #3EB1DD;">Poder mágico/magia:</b>
		Aumenta el poder de ataques mágicos. Si posees mas magia que fuerza, tu personaje golpeará únicamente con ataques mágicos.
	</li>
	
	<li style="margin-bottom: 10px;">
		<b style="color: #BC44FF;">Habilidad mágica:</b>
		Aumenta tu velocidad al lanzar magias, pudiendo lograr así múltiples ataques consecutivos.
	</li>
	
	<li style="margin-bottom: 10px;">
		<b style="color: #6649FF;">Contraconjuro:</b>
		Aumenta tu resistencia contra ataques mágicos. Cuanto mas contraconjuro tengas, menos daño te harán los ataques mágicos.
	</li>
</ul>

