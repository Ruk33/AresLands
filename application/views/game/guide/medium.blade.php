<h4>Objetos</h4>
<p>Cuando consigas un nuevo objeto, éste se situará en tu inventario:</p>

<imagen de inventario>

<p>Solo cuando te lo equipes, obtendrás sus bonificaciones (las cuales podrás ver en detalle situando el cursor sobre su imagen). Para equipar un objeto, simplemente debes hacer clic en él, y darle a Equipar. Para sacartelo, clic sobre su imagen y le das a Desequipar. Ten en cuenta que no podrás sacarte un objeto si tienes el inventario lleno.</p>

<p>Los objetos que son de tipo big (una excepción, son los arcos/bow) hacen uso de dos manos, por lo que no será posible utilizar escudo con ellos.</p>