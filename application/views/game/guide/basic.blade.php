<div class="guide-row">
	<h2>Introducción</h2>
	<p class="guide-p">AresLands es un juego de navegador <b>completamente gratuito</b>, que <b>puedes jugar sin necesidad de instalar nada</b>. Su ambiente es <b>épico</b>, y deberás guiar, equipar, completar misiones, entrenar a tu personaje y derrotar a tus enemigos, ¡para ser el mejor!. Recuerda que jugarás junto a otros jugadores que tendrán la misma meta, ¡no debes dejarlos superarte!.</p>
</div>

<div class="guide-row">
	<h2>¿Cómo jugar?</h2>
	<p class="guide-p">Registrarse para comenzar a jugar es muy sencillo y no te tomará mas de 2 minutos.</p>

	<ol>
		<li>Primero, debes ir a la página de <a href="//ironfist.com.ar" target="_blank">IronFist</a>.</li>
		<li>Dirígete al apartado Registro o haz <a href="//ironfist.com.ar/register" target="_blank">clic aquí</a>.</li>
		<li>Completa el formulario.</li>
		<li>¡Ya estás!. Recuerda que con tu cuenta de IronFist ya puedes acceder y jugar a todos nuestros juegos sin que tengas que registrarte nuevamente.</li>
	</ol>
</div>

