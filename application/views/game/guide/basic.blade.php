<div class="guide-row">
	<h2>Secciones</h2>
	<div class="guide-p">
		<div class="guide-row">
			<h4 class="guide-h4">Tu personaje</h4>
			<p class="guide-p">En este apartado, encontrarás la información de tu personaje (como vida, atributos, ubicación, etc.) junto con los objetos que posees (tanto en el inventario como equipados).</p>
		</div>

		<div class="guide-row">
			<h4 class="guide-h4">Mensajes</h4>
			<p class="guide-p">Aquí tendrás la posibilidad de leer y enviar mensajes a otros jugadores. Ten en cuenta que en este apartado será donde recivas noticias en caso de ser atacado o, finalizar una exploración.</p>
		</div>

		<div class="guide-row">
			<h4 class="guide-h4">Viajar</h4>
			<p class="guide-p">En AresLands hay muchas ciudades. Comenzarás con unas pocas y luego, a medida avances y progreses, iras destrabando nuevas. En este apartado tendrás la posibilidad de viajar a diferentes zonas. Ten en cuenta que el viaje tiene un precio de <i class="coin coin-copper" style="display: inline-block;"></i> 10 monedas de cobre.</p>
		</div>

		<div class="guide-row">
			<h4 class="guide-h4">Batallar</h4>
			<div class="guide-p">
				<p>Prueba tu valía batallando contra otros jugadores o completa misiones derrotando mounstruos. Podrás buscar a tu oponente mediante su nombre, aleatoriamente, o aleatoriamente dentro de un grupo.</p>
				<p>Si batallas contra otro personaje y vences, obtendrás oro, experiencia y puntos de PvP (los cuales te ayudan a mejorar tu puesto en el ranking). Si fallas, solo obtendrás experiencia.</p>
			</div>
		</div>

		<div class="guide-row">
			<h4 class="guide-h4">Explorar</h4>
			<p class="guide-p">Explora para obtener recompensas, encontrarte con criaturas y destrabar comerciantes. Cuanto mas tiempo explores, mas efectiva será tu exploración, y por ende serás mejor recompensado. Ten en cuenta, que si exploras, no podrás hacer ninguna otra acción (batallar, viajar, etc.).</p>
		</div>

		<div class="guide-row">
			<h4 class="guide-h4">Talentos</h4>
			<p class="guide-p">Los talentos son habilidades únicas que se te son otorgadas al inicio del juego, cuando terminas de personalizar a tu personaje. En este apartado, podrás elegir cuáles habilidades quieres aprender y se te mostrarán cuáles ya has aprendido/usado.</p>
		</div>

		<div class="guide-row">
			<h4 class="guide-h4">Comercios</h4>
			<p class="guide-p">¿Tienes un objeto que ya no usas?, ¡comercialo a otros jugadores e intenta sacar el mejor provecho!. El comercio entre jugadores, te permite comerciar libremente con otros personajes. Para comerciar, simplemente debes elegir el objeto que quieres vender, el precio y la cantidad de tiempo en la que tu oferta será válida (una vez pasado este tiempo, deberás volver a recoger tu objeto). Cuando logres vender un objeto, se te notificará por mensaje.</p>
		</div>

		<div class="guide-row">
			<h4 class="guide-h4">Ranking</h4>
			<p class="guide-p">El sagrado salón en donde encontrarás a aquellos grupos, guerreros o magos mas fuertes del juego, ¿lograrás ganarte un lugar entre los primeros?. El ranking depende de la cantidad de puntos de pvp que tengas, cuanto mas, mas arriba estarás. Los puntos de pvp los obtienes venciendo a otros jugadores en combate.</p>
		</div>

		<div class="guide-row">
			<h4 class="guide-h4">Orbes</h4>
			<div class="guide-p">
				<p>Conoce los misteriosos orbes y averigua qué personaje es su poseedor. Si crees que puedes vencerlo, atácalo para robarle el misterioso objeto.</p>
				<p>En este apartado no solo se te mostrarán los orbes que puedes conseguir, si no que además se te informará de lo que otorga cada uno (sus recomensas).</p>
			</div>
		</div>

		<div class="guide-row">
			<h4 class="guide-h4">Torneos</h4>
			<p class="guide-p">¿Un torneo entre clanes se aproxima o ya está en juego?, pasa y averigua toda la información al respecto en este apartado. Si el torneo ya está activo, cierta información solo será revelada al finalizar el mismo.</p>
		</div>
	</div>
