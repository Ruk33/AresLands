<h2>Apartados</h2>
<h4>Tu personaje</h4>
<p>En este apartado, encontrarás la información de tu personaje (como vida, atributos, ubicación, etc.) junto con los objetos que posees (tanto en el inventario como equipados).</p>

<h4>Mensajes</h4>
<p>Aquí tendrás la posibilidad de leer y enviar mensajes a otros jugadores. Ten en cuenta que en este apartado será donde recivas noticias en caso de ser atacado o, finalizar una exploración.</p>

<h4>Viajar</h4>
<p>En AresLands hay muchas ciudades, villas y zonas de farmeo a las que podrás viajar ya sea para revisar mercancías, encontrar monstruos y/o completar misiones.</p>

<ul>
	<li><b>Ciudades:</b> En las ciudades podrás encontrar mercaderes, los cuales se encargarán de venderte objetos y casualmente tendrán algún trabajillo para tu personaje. Además, lograrás encontrar algún que otro monstruo, pero es muy raro o serán de muy bajo nivel.</li>
	<li><b>Villas:</b> En las villas se alojan aquellos personajes que te darán misiones (y con ellas, ¡recompensas!) para completar junto con una mayor cantidad de monstruos que en las ciudades.</li>
	<li><b>Zonas de farm/Farm zones:</b> En las zonas de farmeo, es donde se alojan los mas peligrosos monstruos, están plagadas de ellos, ¡ten cuidado!.</li>
	<li><b>Dungeons/mazmorras:</b> Solo pocos se atreven a ir a estas salas de la muerte, y pocos son también los que logran volver... enteros. Pero aquellos héroes que salen victoriosos, obtienen grandes recompensas.</li>
</ul>

<h4>Batallar</h4>
<p>Prueba tu valía batallando contra otros jugadores. Podrás buscar a tu oponente mediante su nombre, aleatoriamente, o aleatoriamente dentro de un grupo.</p>
<p>Luego de encontrar a tu contrincante, si vences, obtendrás oro, experiencia y puntos de pvp (los cuales te ayudan a mejorar tu puesto en el ranking). Si fallas, solo experiencia.</p>

<h4>Explorar</h4>
<p>Explora para obtener recompensas y encontrarte con criaturas. Cuanto mas tiempo explores, mas efectiva será tu exploración, y por ende serás mejor recompensado. Ten en cuenta, que si exploras, no podrás hacer ninguna otra acción (batallar, viajar, etc.).</p>

<h4>Grupo/clan</h4>
<p>Muchos dicen que la unión hace la fuerza. Si tu también lo crees puedes crear, o inclusive, unirte a un grupo para obtener bonificaciones que mejorarán tus destrezas en los combates.</p>

<h4>Comerciar</h4>
<p>¿Tienes un objeto que ya no usas?, ¿no quieres venderlo en los mercaderes porque piensas que te están estafando?, ¡vendelo a otros jugadores e intenta sacar el mejor provecho!. El comercio entre jugadores, te permite comerciar libremente con otros personajes. Cuando creas un nuevo comercio, debes esperar la respuesta de la persona a quien quieres vender, ésta puede aceptar o rechazar tu oferta. Si alguna de estas acciones es llevada a cabo, se te notificará mediante un mensaje.</p>

<h4>Personajes</h4>
<p>Esta sección está diseñada especialmente para que puedas encontrar el personaje que estás buscando. Tienes la posibilidad de filtrarlos por nombre, nombre del grupo al que pertenezcan, ordenarlos alfabéticamente o por sus puntos de pvp.</p>

<h4>Ranking</h4>
<p>El sagrado salón en donde encontrarás a aquellos guerreros o magos mas fuertes del juego, ¿lograrás ganarte un lugar entre los primeros?. El ranking depende de la cantidad de puntos de pvp que tengas, cuanto mas, mas arriba estarás. Los puntos de pvp los obtienes venciendo a otros jugadores en combate.</p>


<h2>Mecánicas</h2>
<h4>Misiones</h4>
<p>Algunas criaturas necesitan realizar algunos trabajos, ya sean de asesinato por la noche, llevar objetos, hablar con otras criaturas, enviar un mensaje y demás, por lo que contratan a personajes para realizarlos y pagarles una considerable recompensa al finalizarlos.</p>

<h4>Atributos</h4>
<p>Los atributos son los aspectos en los que puedes mejorar a tu personaje. Dependiendo de como los distribuyas los resultados serán diferentes.</p>

<ul>
	<li><b>HP:</b> Son los puntos de salud del personaje. Estos se verán incrementados con cada nivel que suba el personaje, además se pueden aumentar más subiendo el atributo Vitalidad.</li>
	<li><b>Vitalidad:</b> Ayudan a aumentar la salud del personaje en combate y la regeneración de la misma una vez finalizado.</li>
	<li><b>Destreza:</b> Aumenta la velocidad de ataque del personaje, reduciendo así el tiempo que el personaje tarda en volver a golpear. Esto permite al personaje dar golpes consecutivos.</li>
	<li><b>Magia:</b> Aumenta el poder de ataque mágico del personaje, permitiendo así efectuar un daño mágico mayor al enemigo.</li>
	<li><b>Fuerza:</b> Aumenta el poder de ataque físico del personaje, permitiendo así efectuar un daño físico mayor al enemigo.</li>
	<li><b>Suerte:</b> Aumenta la probabilidad de asestar golpes críticos, en otras palabras le da al personaje más chances de dar golpes del doble o triple del golpe real.</li>
</ul>

