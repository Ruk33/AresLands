<h2>¿Qué es AresLands?</h2>
<p>AresLands es un juego de navegador completamente gratuito, que puedes jugar sin necesidad de instalar nada. Su ambiente es épico, y deberás guiar, equipar, completar misiones, entrenar a tu personaje y derrotar a tus enemigos, ¡para ser el mejor!. Recuerda que jugarás junto a otros jugadores que tendrán la misma meta, ¡no debes dejarlos superarte!.</p>


<h2>Registro</h2>
<p>Registrarse para comenzar a jugar es muy sencillo y no te tomará mas de 2 minutos.</p>

<ol>
	<li>Primero, debes ir a la página de IronFist.</li>
	<li>Dirígete al apartado Registro o haz clic aquí.</li>
	<li>Completa el formulario.</li>
	<li>¡Ya estás!. Recuerda que con tu cuenta de IronFist ya puedes acceder y jugar a todos nuestros juegos sin que tengas que registrarte nuevamente.</li>
</ol>


<h2>Razas</h2>
<p>En el mundo de AresLands, cuatro son las razas que se distinguen por su gran poderío, destreza e inteligencia. Solo podrás elegir una de ellas así que, ¡elige con sabiduría!.</p>

<h4>Enanos</h4>
<p>Señores de la roca y maestros en el arte de la guerra. Sus fornidos cuerpos les dan una implacable resistencia física, diestros en el uso de armas a dos manos. Su fortaleza es la fuerza misma y su debilidad es su carencia de magia en su sangre. Excelentes herreros, solo ambicionan una cosa.... LA CONQUISTA.</p>
<p>Los Enanos están basados en la Fuerza, buena cantidad de vida.</p>
 
<ul>
	<li><b>HP:</b> 500</li>
	<li><b>Vitalidad:</b> 18</li>
	<li><b>Destreza:</b> 5</li>
	<li><b>Magia:</b> 3</li>
	<li><b>Fuerza:</b> 28</li>
	<li><b>Suerte:</b> 5</li>
</ul>

<h4>Humanos</h4>
<p>Diestros tanto en el combate cuerpo a cuerpo como en la magia. Respetan a los dioses pero no a un alto grado. La guerra es su mayor fuente de ingresos, con buenas herrerias y excelentes líderes, son una de las razas más gloriosas y duras en el combate.</p>
<p>Los humanos son el equivalente medio entre los Enanos y los Drows, pero con una inclinación a la fuerza.</p>

